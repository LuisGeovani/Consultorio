public class Especialidade {
    // Atributos
    private long id;
    private String nome;

    // Métodos acessores
    public long getId() {
        return this.id;
    }
    public String getNome() {
        return this.nome;
    }

    // Métodos modificadores
    public void setId(long id) {
        this.id = id;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
}